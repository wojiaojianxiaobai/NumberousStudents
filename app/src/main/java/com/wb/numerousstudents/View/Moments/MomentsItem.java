package com.wb.numerousstudents.View.Moments;

public class MomentsItem {
    private String mName;
    private int mUserId;

    public MomentsItem(String name, int userId){
        mName = name;
        mUserId = mUserId;
    }

    public String getName(){
        return mName;
    }

    public int getUserId(){
        return mUserId;
    }

}
